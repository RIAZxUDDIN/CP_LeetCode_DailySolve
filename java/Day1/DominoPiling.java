package Day1;
import java.util.Scanner;

public class DominoPiling {
    public static void main(String[] args) {
        //Problem: https://codeforces.com/problemset/problem/50/A
        Scanner sc = new Scanner(System.in);
        int m,n;
        m = sc.nextInt();
        n = sc.nextInt();
        System.out.println((m*n)/2);
    }
}
